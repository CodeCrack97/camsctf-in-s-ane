charset="0123456789ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz"

def conv(num, base):
    result = ''
    while num != 0:
        result = charset[num % base] + result
        num /= base
    return result

